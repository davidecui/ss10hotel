package model;

public class ParametriStruttura {
	
private int numeroPiani;
	
private int numeroSingolePerPiano;
	
private int numeroDoppiePerPiano;
	
private int numeroSuitePerPiano;

public int getNumeroPiani() {
	return numeroPiani;
}

public void setNumeroPiani(int numeroPiani) {
	this.numeroPiani = numeroPiani;
}

public int getNumeroSingolePerPiano() {
	return numeroSingolePerPiano;
}

public void setNumeroSingolePerPiano(int numeroSingolePerPiano) {
	this.numeroSingolePerPiano = numeroSingolePerPiano;
}

public int getNumeroDoppiePerPiano() {
	return numeroDoppiePerPiano;
}

public void setNumeroDoppiePerPiano(int numeroDoppiePerPiano) {
	this.numeroDoppiePerPiano = numeroDoppiePerPiano;
}

public int getNumeroSuitePerPiano() {
	return numeroSuitePerPiano;
}

public void setNumeroSuitePerPiano(int numeroSuitePerPiano) {
	this.numeroSuitePerPiano = numeroSuitePerPiano;
}



	

}
